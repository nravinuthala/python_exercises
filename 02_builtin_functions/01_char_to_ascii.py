# Character to Ascii value
# Write a program to take any character from the user and print its corresponding ascii value.

# Note : Use ord() function.

# Input format:
# Input consists of any character including 'a' to 'z', 'A' to 'Z', '0' to '9'and special characters.

# Output format:
# The output consists of the corresponding integer value of the entered character.

# [All texts in bold corresponds to input and rest all are outputs]

# Sample Input and Output 1:
# Enter the character
# P
# The Ascii value of the character is: 80

# Sample Input and Output 2:
# Enter the character
# *
# The Ascii value of the character is: 42

# Sample Input and Output 3:
# Enter the character
# 1
# The Ascii value of the character is: 49

