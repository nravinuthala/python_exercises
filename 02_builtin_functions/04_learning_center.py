# Learning center
 

# Holy Cross-Play Buds is a famous kid learning center in the area. They are running successfully with 30+ kids already enrolled. As the next leap to their success, they decided to play transport vehicles for pick up and drop off the kids at their homes in a real safe and secure manner.

# The School authorities initially formulated 2 different routes to and fro. Next, they decided on the point of pick up & drop along the route. Finally, they wanted to calculate the distance of the pick-up & drop point from the school based on which they would frame the transportation charges.

# A volunteer Parent of their school came up to write a program to automate this process. Assuming the school is present at Origin, of point (0,0), if given the x coordinate and y-coordinate of the pick-up drop point, the program should fetch the distance from the school.

# Input Format:

# The first input is a string as a pick-up and drop point name.
# The second and third inputs are integer values to get the X and Y coordinates of the point.

# Output Format:
# Output should be the distance of the point from school and it should be printed correctly to two decimals.

# Note: Use format() function to display the values

# Sample Input 1:
# Silk Board
# 5
# 8
# Sample Output 1:
# 9.43

 

# Sample Input 2:
# Halsey Road
# 6
# 0
# Sample Output 2:
# 6.00

# Problem Requirements:

# Keyword	    Min Count	Max Count
# .format	    1	        -
