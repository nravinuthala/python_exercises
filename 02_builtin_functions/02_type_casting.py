# Conversion from Float to Integer & Vice-versa


# Write a program to take user input as either float or integer value and convert it to both integer and float.

# Note : Use int() and float() function to convert the values.

 

# Input Format :

# Input consists of either integer or float value

# Output Format:
# First line of the output has to display integer value.
# Second line of the output has to display float value.

# [All texts in bold corresponds to input and rest all are outputs]

# Sample Input and Output 1:

# Enter the value:

# 36.8

# The integer value is: 36

# The float value is: 36.8

# Sample Input and Output 2:

# Enter the value:

# 6

# The integer value is: 6

# The float value is: 6.0

# Problem Requirements:

# Keyword	    Min Count	Max Count
# float	    1	        -
# int	        1	        -