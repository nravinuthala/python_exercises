
# Octal, Binary, and Hexadecimal


# Write a program to display the octal, binary, and hexadecimal equivalent, for the given input of integer value.

# Note : Use oct(), hex() and bin() functions for conversion.
 

# Input Format:
# Input consists ofinteger value.

# Output Format:
# First line of the output has to display octal equivalent to the given integer.
# Second line of the output has to display binary equivalent to the given integer.
# Third line of the output has to display hexadecimal equivalent to the given integer.

# [Refer to sample input and output]

# [All the text in bold refer to input and rest are output]

 

# Sample Input and Output 1:

# Enter the value: 

# 65

# Octal equivalent of 65 is 0o101
# Binary equivalent of 65 is 0b1000001
# Hexadecimal equivalent of 65 is 0x41
 

# Sample Input and Output 2:

# Enter the value: 

# 122

# Octal equivalent of 122 is 0o172
# Binary equivalent of 122 is 0b1111010
# Hexadecimal equivalent of 122 is 0x7a

# Problem Requirements:

# Keyword	    Min Count	Max Count
# bin	        1	        -
# hex	        1	        -
# oct	        1	        -