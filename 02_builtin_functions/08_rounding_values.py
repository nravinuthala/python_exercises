
# Rounding up values


# Write a program to round up the values given by the user as an input (it can be an integer or float). Also, display the result based on the correct round-up up to whatever places the user will give as input.

 

# Input Format :


# First line of the input consists of float values, N.
# Second line of the input consists of an integer value, which represents the number of decimal places (D) for round up.

# Note: Number of decimal places in N must be greater than D. Use round() function.

# Output Format:


# First line of the output has to display the round up value for the given float value.

# [All text in bold corresponds to input and rest corresponds to output.]

 

# Sample Input and Output  1:

# Enter the value to be rounded up :

# 2.678

# Enter the place to which it should be rounded up :

# 2

# The round up value is: 2.68

# Sample Input and Output  2:

# Enter the value to be rounded up :

# 15.461

# Enter the place to which it should be rounded up :

# 1

# The round up value is: 15.5

# Problem Requirements:

# Keyword	    Min Count	Max Count
# round	    1	        -  
