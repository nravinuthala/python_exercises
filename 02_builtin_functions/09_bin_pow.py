# Binary and power using built-in functions

 

# Write a program to calculate the binary and power of given user input.

 

# Input Format :

# First line of the input consists of an integer value, N.

# Second line of the input consists of the integer value which is used to calculate the power, P.

# Output Format:

# First line of the output has to display the binary value for the given integer input.

# Second line of the output has to display the power value, NP.

# Note: Use bin() and pow() functions.

# [All texts in bold are inputs and the rest corresponds to output.]

 

# Sample Input and Output 1:

# Enter the value :

# 10

# Enter the number to what you want to find the power :

# 2

# The binary of the number is: 0b1010 

# The power of the number is: 100

 

# Sample Input and Output 2:

# Enter the value :

# 15

# Enter the number to what you want to find the power :

# 6

# The binary of the number is: 0b1111

# The power of the number is: 11390625

