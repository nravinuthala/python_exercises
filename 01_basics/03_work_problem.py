# Work problem


# An employee was engaged on a job for N days on the condition that they would get a wage of Rs. X for the day they work, but they have to pay a fine of Rs. Y for each day of their absence. If they get Rs. M at the end, means they were absent for work for ___days.

    


# Input Format:


# 1st input is an integer indicates the number of working days.
# 2nd input is an integer indicates the wage of man per day.
# 3rd input is an integer indicates the fine per day for absence.
# 4th input is an integer indicates total amount an employee gets at the end.

# Output Format:


# The output is an integer indicates the number of days he is absent.

# Sample Input 1:
# 30
# 10
# 2
# 216


# Sample Output 1 :
# 7

# Sample Input 2:
# 30
# 20
# 2
# 300


# Sample Output 2:
# 14